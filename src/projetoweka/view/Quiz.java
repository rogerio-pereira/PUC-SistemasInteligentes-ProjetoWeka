    private void refreshDados()
    {
        Perguntas pergunta = perguntasController.getPerguntas().get(perguntaIndex);
        
        this.setTitle("Pergunta "+(perguntaIndex+1)+": "+pergunta.getPergunta());
        labelPergunta.setText(pergunta.getPergunta());
        comboAlternativas.removeAllItems();
            comboAlternativas.addItem("");
        for(String alternativa : pergunta.getAlternativas())
            comboAlternativas.addItem(alternativa);
        
        atualizaBotoes();
    }
