    private void botaoAvancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAvancarActionPerformed
        if(isRespostaCorrect()) {
            perguntaIndex++;
            refreshDados();
        }
    }//GEN-LAST:event_botaoAvancarActionPerformed

    private void botaoVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoVoltarActionPerformed
        perguntaIndex--;
        refreshDados();
    }//GEN-LAST:event_botaoVoltarActionPerformed

    private void botaoFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFinalizarActionPerformed
        try {
            if(isRespostaCorrect()) {
                String resposta = dadosController.classifica(respostas);
                
                JOptionPane.showMessageDialog(
                                            this, 
                                            "Sua casa é: "+resposta, 
                                            "Resposta", 
                                            JOptionPane.INFORMATION_MESSAGE, 
                                            new ImageIcon("img/success-circle.png")
                                        );
                
                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                                            this, 
                                            "Erro ao classificar dados"+
                                                    "\nErro: "+ex.getMessage()+
                                                    "\nCausa: "+ex.getCause(), 
                                            "Erro", 
                                            JOptionPane.INFORMATION_MESSAGE, 
                                            new ImageIcon("img/error-circle.png")
                                        );
        }
    }//GEN-LAST:event_botaoFinalizarActionPerformed

    private void atualizaBotoes()
    {
        if(perguntaIndex == 0)
            botaoVoltar.setEnabled(false);
        else
            botaoVoltar.setEnabled(true);
        
        if(perguntaIndex < (perguntasController.getPerguntas().size() - 1)) {
            botaoAvancar.setEnabled(true);
            botaoFinalizar.setEnabled(false);
        }
        else {
            botaoAvancar.setEnabled(false);
            botaoFinalizar.setEnabled(true);
        }
    }
    
    private boolean isRespostaCorrect()
    {
        int resposta = comboAlternativas.getSelectedIndex();
        
        if(resposta > 0) {
            respostas[perguntaIndex] = resposta;
            
            return true;
        }
        else {
            JOptionPane.showMessageDialog(
                                            this, 
                                            "Selecione uma opção", 
                                            "Alerta", 
                                            JOptionPane.INFORMATION_MESSAGE, 
                                            new ImageIcon("img/alert-circle.png")
                                        );
            
            return false;
        }
    }

